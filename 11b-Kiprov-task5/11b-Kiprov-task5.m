%създава масив от данни z, съдържащ 
%числата от 0 до 2 пъти π, със стъпка π/200
%изчислява косинуса на масива
%изчислява синуса на масива
%чертае графика на наредена тройка(x,y,z) с '+'/без '+'(точка 4 и точка 5)
function pl3d 
  z = [0:pi/200:2*pi]
  x = cos(z)
  y = sin(z)
  plot3(x,y,z)
  plot3(x,y,z,'+')
end